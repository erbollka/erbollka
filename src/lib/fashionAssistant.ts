export function buildFashionPrompt(question: string, context: FashionAssistantContext, kpis: WeeklyKpi[]) {
  const insights = buildKpiInsights(kpis);
  const weakest = insights.slice(0, 3);
  const discountShare = percent(context.discount, context.sales);
  const returnsShare = percent(context.returns, context.sales);
  const profitShare = percent(context.profit, context.sales);
  const topFindings = context.findings?.slice(0, 6) ?? [];

  return [
    `Вопрос команды: ${question}`,
    "Контекст магазина:",
    `- продажи: ${context.sales}`,
    `- прибыль: ${context.profit} (${profitShare}% от продаж)`,
    `- скидки: ${context.discount} (${discountShare}% от продаж)`,
    `- возвраты: ${context.returns} (${returnsShare}% от продаж)`,
    `- риск аудита: ${context.riskLevel}`,
    `- качество отчета: ${context.qualityScore ?? "нет оценки"}`,
    `- листы отчета: ${context.sheetNames?.join(", ") || "нет данных"}`,
    `- итог аудита: ${context.summary || "нет загруженного отчета"}`,
    "KPI недели:",
    ...insights.map(
      (item) =>
        `- ${item.label}: ${formatKpiValue(item.current, item.unit)} из ${formatKpiValue(item.target, item.unit)}, выполнение ${item.progress}%, разрыв ${formatKpiValue(item.gap, item.unit)}`,
    ),
    "Главные разрывы:",
    ...(weakest.length ? weakest.map((item) => `- ${item.label}: не хватает ${formatKpiValue(item.gap, item.unit)}`) : ["- KPI выглядят закрытыми"]),
    "Рекомендации аудита:",
    ...(context.recommendations?.length ? context.recommendations.slice(0, 5).map((item) => `- ${item}`) : ["- нет рекомендаций"]),
    "Проблемы из отчета:",
    ...(topFindings.length
      ? topFindings.map(
          (item) =>
            `- ${item.severity}: ${item.title}; ${item.description}; исправление: ${item.suggestedFix}; место: ${item.sheetName}${item.cell ? ` ${item.cell}` : ""}`,
        )
      : ["- нет найденных проблем"]),
    "Встроенный retail-playbook для расширения ответа:",
    `- клиентские поводы: ${retailPlaybook.customerOccasions.join(", ")}`,
    `- мерчандайзинг: ${retailPlaybook.merchandising.join(", ")}`,
    `- ритуалы команды: ${retailPlaybook.teamRituals.join(", ")}`,
    `- риск-сигналы: ${retailPlaybook.riskSignals.join(", ")}`,
    "Ответь структурно:",
    "1. Главный вывод.",
    "2. План на 7 дней.",
    "3. Что сказать команде.",
    "4. Что проверить в цифрах/аудите.",
    "5. Идея для витрины/атмосферы магазина.",
  ].join("\n");
}

