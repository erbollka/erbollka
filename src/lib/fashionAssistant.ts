export function formatCompactMoney(value: number) {
  return new Intl.NumberFormat("ru-RU", {
    notation: "compact",
    maximumFractionDigits: 1,
  }).format(value);
}

function buildKpiInsights(kpis: WeeklyKpi[]): KpiInsight[] {
  return kpis
    .map((item) => {
      const gap = Math.max(0, item.target - item.current);
      const progress = item.target > 0 ? Math.min(999, Math.round((item.current / item.target) * 100)) : 0;
      return { ...item, gap, progress };
    })
    .sort((a, b) => {
      const aGapRatio = a.target > 0 ? a.gap / a.target : 0;
      const bGapRatio = b.target > 0 ? b.gap / b.target : 0;
      return bGapRatio - aGapRatio;
    });
}

function summarizeAuditFocus(context: FashionAssistantContext) {
  const finding = context.findings?.[0];
  if (finding) {
    return `${finding.title} на листе ${finding.sheetName}${finding.cell ? `, ячейка ${finding.cell}` : ""}. ${finding.suggestedFix}`;
  }
  return context.recommendations?.[0] ?? "проверьте итоги, скидки, возвраты и дубли строк";
}

function formatKpiValue(value: number, unit: string) {
  if (unit.includes("₸")) return `${formatCompactMoney(value)} ₸`;
  return `${value}${unit}`;
}

function percent(value: number, total: number) {
  return total > 0 ? Math.round((value / total) * 100) : 0;
}

function hasAny(text: string, patterns: string[]) {
  return patterns.some((pattern) => text.includes(pattern));
}

function buildAdaptiveOpening(question: string, focus: string) {
  const cleanQuestion = question.trim().replace(/\s+/g, " ");
  if (!cleanQuestion) {
    return `Главный вывод: сейчас лучше держать фокус на ${focus} и каждый день связывать действия команды с цифрами.`;
  }
  return `Я поняла вопрос так: “${cleanQuestion}”. Главный вывод: отвечать на него нужно через ${focus}, клиентский повод и контроль денег, а не общей мотивацией.`;
}

function buildAdaptiveActions(question: string, context: FashionAssistantContext) {
  const actions: string[] = [];

  if (hasAny(question, ["почему", "причин", "из-за", "просел", "упал", "сниз"])) {
    actions.push("1. Сначала разложите проблему на причины: трафик, конверсия, средний чек, скидки, возвраты, наличие размеров.");
    actions.push("2. Не делайте вывод по одной цифре: если продажи ниже, но скидки и возвраты выше, проблема может быть не в спросе, а в качестве продаж.");
  } else if (hasAny(question, ["как", "что сделать", "поднять", "улучш", "увелич"])) {
    actions.push("1. Выберите одно действие на ближайшие 48 часов, которое продавцы реально выполнят: товар недели, комплект недели или скрипт под один клиентский повод.");
    actions.push("2. Привяжите действие к измеримой цифре: средний чек, конверсия, допродажи, скидки или возвраты.");
  } else if (hasAny(question, ["стоит ли", "можно ли", "нужно ли", "лучше"])) {
    actions.push("1. Примите решение через простой фильтр: даст ли это больше ценности клиенту, не съест ли маржу, сможет ли команда объяснить это за 20 секунд.");
    actions.push("2. Если решение связано со скидкой, сначала попробуйте альтернативу: комплект, подарок-мелочь, бесплатный подбор образа, перенос товара в более сильную зону.");
  } else {
    actions.push("1. Сформулируйте гипотезу: что именно должно измениться в поведении клиента или продавца после этого решения.");
    actions.push("2. Проверьте гипотезу маленьким тестом на 3-7 дней, а не меняйте сразу весь магазин.");
  }

  if (hasAny(question, ["размер", "посад", "пример", "не подходит"])) {
    actions.push("3. Введите журнал отказов: размер, посадка, цвет, цена, не с чем сочетать. Через неделю это даст подсказку по закупу и витрине.");
  } else if (hasAny(question, ["instagram", "инст", "соц", "контент", "сторис", "фото"])) {
    actions.push("3. Для контента снимайте не отдельную вещь, а ситуацию: семейный выход, работа, подарок, вечер. Под каждым образом пишите, кому он подойдет.");
  } else if (hasAny(question, ["прем", "зарплат", "мотивац", "план"])) {
    actions.push("3. Мотивацию команды привяжите не только к выручке, но и к качеству: допродажи, низкие необоснованные скидки, возвраты, выполнение личных микро-целей.");
  } else if (hasAny(question, ["клиент", "покупател", "возраж", "дорого"])) {
    actions.push("3. На возражение “дорого” отвечайте не скидкой, а ценностью: посадка, сочетания, сколько раз вещь можно носить, какие образы она закрывает.");
  } else {
    actions.push("3. Добавьте клиентский повод: работа, семья, выходной, подарок, поездка. Без повода товар выглядит просто вещью, с поводом - решением.");
  }

  if (context.discount > 0) {
    actions.push("4. Отдельно контролируйте скидки: если продавцы часто начинают с дисконта, замените это правилом “сначала образ, потом цена”.");
  }
  if (context.returns > 0) {
    actions.push("5. Возвраты разбирайте не как неприятность, а как сигнал: не тот размер, ожидание, качество консультации или ошибка в продаже.");
  }

  return actions.slice(0, 5);
}
